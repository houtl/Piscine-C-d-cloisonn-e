int	ft_find_next_prime(int nb)
{
	int i;

	if (nb <= 2)
		return (2);
	else if (nb == 3)
		return (3);
	else
	{
		i = 1;
		while (i * i < nb)
		{
			i = i + 1;
			if (nb % i == 0)
			{
				nb = nb + 1;
				i = 1;
			}
		}
		return (nb);
	}
}
