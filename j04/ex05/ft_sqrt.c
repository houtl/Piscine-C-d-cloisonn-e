int	ft_sqrt(int nb)
{
	int i;
	int j;
	int k;

	if (nb > 0)
	{
		i = 1;
		j = 46342;
		while (1)
		{
			k = (i + j) / 2;
			if (k * k == nb)
				return (k);
			else if (k * k > nb)
				j = k;
			else
				i = k;
			if (j - i <= 1)
				return (0);
		}
	}
	else
		return (0);
}
