#include <unistd.h>

void	print_bits(unsigned char octet)
{
	int		i;
	int		j;
	char	c[9];

	i = octet + 0;
	j = 0;
	while (j < 8)
	{
		c[j++] = i % 2 + 48;
		i = i / 2;
	}
	c[j] = 0;
	while (j > 0)
	{
		write(1, &c[j-1], 1);
		j--;
	}
}
