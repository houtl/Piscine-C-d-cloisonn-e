#include <unistd.h>

int main(int ac, char **av)
{
	char	*c;

	if (ac != 2)
	{
		write(1, "\n", 1);
		return (0);
	}
	c = av[1];
	while (*c)
	{
		if ((*c > 64 && *c < 78) || (*c > 96 && *c < 110))
			*c += 13;
		else if ((*c > 77 && *c < 91) || (*c > 109 && *c < 123))
			*c -= 13;
		write(1, c, 1);
		c++;
	}
	write (1, "\n", 1);
	return (0);
}
