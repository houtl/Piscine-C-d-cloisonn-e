unsigned char	swap_bits(unsigned char octet)
{
	int		i;
	int		j;
	int		n;
	char	dest;

	i = 0 + octet;
	n = 16;
	dest = 0;
	while (i != 0)
	{
		if (n > 128)
			n = 1;
		j = i % 2;
		dest += n * j;
		n = n * 2;
		i = i / 2;
	}
	return (dest);
}
