#include <stdio.h>
#include <stdlib.h>

int main(int ac, char **av)
{
	int a;
	int b;
	int c;

	if (ac != 3)
	{
		printf("\n");
		return (0);
	}
	a = atoi(av[1]);
	b = atoi(av[2]);
	c = a % b;
	while (c)
	{
		a = b;
		b = c;
		c = a % b;
	}
		printf("%d\n", b);
}
