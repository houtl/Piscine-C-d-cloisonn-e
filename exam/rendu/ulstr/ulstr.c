#include <unistd.h>

int main(int i, char **j)
{
	int k;

	k = 0;
	if (i != 2)
	{
		write(1, "\n", 1);
		return (0);
	}
	while (j[1][k] != 0)
	{
		if (j[1][k] >= 65 && j[1][k] <= 90)
			j[1][k] = j[1][k] + 32;
		else if (j[1][k] >= 97 && j[1][k] <= 122)
			j[1][k] = j[1][k] - 32;
		write(1, &j[1][k], 1);
		k++;
	}
	write(1, "\n", 1);
	return (0);
}
