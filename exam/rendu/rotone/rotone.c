#include <unistd.h>

int main(int ac, char **av)
{
	int	i;
	char c;

	i = 0;
	if (ac != 2)
	{
		write(1, "\n", 1);
		return (0);
	}
	while(av[1][i] != 0)
	{
		c = av[1][i];
		if ((c > 64 && c < 90) || (c > 96 && c < 122))
			c = c + 1;
		else if (av[1][i] == 90 || av[1][i] == 122)
			c = c - 25;
		write(1, &c, 1);
		i++;
	}
	write(1, "\n", 1);
}
