int	ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int	j;

	i = 0;
	j = 0;
	while (tab && tab[i])
	{
		if (f(tab[i]) == 1)
			j++;
		i++;
	}
	return (j);
}
