#include <unistd.h>

void	ft_putchar(char c);

int		main(int i, char **j)
{
	int k;
	int l;

	k = i - 1;
	while (k > 0)
	{
		l = 0;
		while (j[k][l] != '\0')
		{
			ft_putchar(j[k][l]);
			l++;
		}
		ft_putchar('\n');
		k--;
	}
}
